function talukCodeNumber(value) {
  const match = String(value || "").match(/(\d+)/);
  return match ? Number(match[1]) : Number.MAX_SAFE_INTEGER;
}

function getNamedTaluksForDistrict(district) {
  const sourceDistrict = talukDistrictAliases[district] || district;
  return Object.keys(tamilNaduLocationCodes[sourceDistrict]?.taluks || {});
}

const generatedTalukLabels = Object.fromEntries(
  Object.entries(tamilNaduLocations).map(([district, taluks]) => {
    const importedKeys = Object.keys(taluks || {}).sort((a, b) => talukCodeNumber(a) - talukCodeNumber(b));
    const namedTaluks = getNamedTaluksForDistrict(district);
    const override = talukNameOverrides[district] || {};
    const labels = {};
    importedKeys.forEach((talukKey, index) => {
      labels[talukKey] = override[talukKey] || namedTaluks[index] || talukKey;
    });
    return [district, labels];
  })
);

export function getTalukLabel(district, taluk) {
  if (!taluk) return "";
  return generatedTalukLabels[district]?.[taluk] || taluk;
}

